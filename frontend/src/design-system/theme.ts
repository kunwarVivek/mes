/**
 * Design System Theme
 *
 * Centralized theme configuration following design principles:
 * - Consistency: Unified visual language
 * - Accessibility: WCAG AA compliant colors
 * - Scalability: Token-based system
 */

export type ThemeMode = 'blue' | 'purple' | 'green' | 'orange' | 'custom'

interface ColorScale {
  50: string
  100: string
  200: string
  300: string
  400: string
  500: string
  600: string
  700: string
  800: string
  900: string
  950: string
}

interface SemanticColors {
  success: string
  warning: string
  error: string
  info: string
  background: string
  surface: string
  border: string
}

interface ManufacturingColors {
  machine: {
    running: string
    idle: string
    down: string
    maintenance: string
  }
  quality: {
    pass: string
    fail: string
    pending: string
    inspection: string
  }
  priority: {
    low: string
    medium: string
    high: string
    critical: string
  }
  severity: {
    minor: string
    moderate: string
    major: string
    critical: string
  }
}

interface ThemeColors extends SemanticColors, ManufacturingColors {
  primary: ColorScale
  secondary: ColorScale
  accent: ColorScale
  neutral: ColorScale
}

export interface ThemeConfig {
  colors: ThemeColors
  typography: {
    fontFamily: {
      sans: string
      mono: string
    }
    fontSize: Record<string, string>
    fontWeight: Record<string, number>
    lineHeight: Record<string, number>
  }
  spacing: Record<string, string>
  borderRadius: Record<string, string>
  shadows: Record<string, string>
  transitions: Record<string, string>
  breakpoints: Record<string, string>
}

